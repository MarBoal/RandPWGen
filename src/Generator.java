class Generator {
    int randomNum;
    String RandomChar;
    String password;
    public int getRandomNum() {
        return randomNum;
    }

    public String getRandomChar() {
        return RandomChar;
    }

    public String getPassword() {
        return password;
    }

    public void setRandomNum(int randomNum) {
        this.randomNum = randomNum;
    }

    public String toString() {
        return password;
    }
}